class IjCoa {
    //Convert Coa to Coa New
    static function stripCoa($str){
        if(!$str) return false;
        $marCoa=array("011","0111","01111","011111","011112","01112","01113","01114","01115","01118","0112","01121",
            "01122","01123","01128","021","0211","02111","021111","021112","02112","021121","021122","021128"
        ,"02113","021131","021132","02114",
            "02115","02118","0212","02121","02122",
            "02123","02124","02125","02126","02128");

        $marCoaNew=array("017","0171","01711","017111","017112","01712","01713","01714","01715","01718","0172"
        ,"01721","01722","01723","01728","027","0271",
            "02711","027111","027112","02712","027121","027122","027128","02713","027131","027132","02714",
            "02715","02718","0272","02721","02722",
            "02723","02724","02725","02726","02728");

